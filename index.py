import turtle
tina = turtle.Turtle()
tina.shape('turtle')

tina.write("I start at 0, 0")
tina.color("orange")
tina.goto(100,100)
tina.write("This is 100, 100")
tina.color("red")
tina.goto(-100,100)
tina.write("This is -100, 100")
tina.color("green")
tina.goto(100,-100)
tina.write("This is 100, -100")
tina.color("blue")
tina.goto(-100,-100)
tina.write("This is -100, -100")
tina.color("violet")
tina.goto(0,0)

